<script lang="ts">
import { defineComponent } from "vue";
import store from "@/store";
import {useRoute} from "vue-router";
import router from "@/router";

export default defineComponent({
  name: "Navigation",
  setup() {
    const route = useRoute();
    //检查现在处于的状态
    // console.log(route.path);
    if (route.path === "/home") {
      store.state.navigation.Home = true;
    } else if (route.path === "/my") {
      store.state.navigation.My = true;
    }

    //前往点击的页面
    const toHome = (): void => {
      store.state.navigation.Home = true;
      store.state.navigation.My = false;
      router.push("/home");
    }
    const toMy = (): void => {
      store.state.navigation.Home = false;
      store.state.navigation.My = true;
      router.push("/my");
    }
    return {
      store,
      toHome,toMy
    }
  }
})
</script>

